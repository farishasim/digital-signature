def get_public_key_file(filename):
    #open text file
    text_file = open(filename, "w")
    #write public key
    text_file.write('Hello World!')
    #close file
    text_file.close()

def get_private_key_file(filename):
    #open text file
    text_file = open(filename, "w")
    #write private key
    text_file.write('Hello World!')
    #close file
    text_file.close()